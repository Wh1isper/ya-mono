class ModelConfig(BaseModel):
    """Model configuration for context management.

    Extensibility:
        This class supports two extension patterns:

        1. Inheritance (recommended for type safety)::

            class MyModelConfig(ModelConfig):
                custom_threshold: float = 0.8

            class MyContext(AgentContext):
                model_cfg: MyModelConfig = Field(default_factory=MyModelConfig)

        2. Extra attributes (for quick prototyping)::

            config = ModelConfig(custom_field="value")
            config.custom_field  # Accessible but not type-checked
    """

    model_config = {"arbitrary_types_allowed": True, "extra": "allow"}

    context_window: int | None = None
    """Total context window size in tokens."""

    proactive_context_management_threshold: float | None = Field(default=0.5, ge=0.0, le=1.0)
    """Proactive context management threshold. When token usage exceeds this ratio, reminders are triggered."""

    compact_threshold: float = Field(default=0.90, ge=0.0, le=1.0)
    """Compact threshold for auto-compaction. When token usage exceeds this ratio, compact is triggered."""

    cold_start_trim_seconds: int = 3600
    """Seconds of inactivity after which KV cache is considered expired.

    When the gap between the last model response timestamp and now exceeds
    this value, all large tool return content is truncated before sending to
    the model, saving input token cost on cold re-encoding.
    Set to 0 to disable.
    """

    max_images: int = 20
    """Maximum number of images allowed in message history. Default is 20 (Claude's limit)."""

    max_videos: int = 1
    """Maximum number of videos allowed in message history. Default is 1."""

    support_gif: bool = True
    """Whether the model supports GIF images. If False, GIF images will be filtered out."""

    max_image_bytes: int = 5 * 1024 * 1024
    """Maximum allowed size in bytes for a single image as seen by the API.

    Most model APIs transmit images as base64-encoded payloads, which are
    roughly 4/3x larger than the raw bytes.  The compression filter
    automatically accounts for this overhead: it targets a raw-byte budget
    of ``max_image_bytes * 3 // 4`` so the encoded result stays within the
    API limit.

    When set to a positive value, binary images whose *encoded* size would
    exceed this limit are compressed (converted to JPEG with reduced quality
    and/or resized) before being sent to the model.  Set to 0 to disable
    compression.

    Default is 5 MB (5_242_880 bytes), matching the limit of most providers
    (e.g. Anthropic, GCP Vertex AI).
    """

    split_large_images: bool = True
    """Whether to split oversized binary images before sending to model."""

    image_split_max_height: int = 4096
    """Maximum height (in pixels) for each image segment when splitting large images."""

    image_split_overlap: int = 50
    """Overlap (in pixels) between adjacent image segments when splitting."""

    capabilities: set[ModelCapability] = Field(default_factory=set)
    """Set of capabilities supported by the model."""

    def has_capability(self, capability: ModelCapability) -> bool:
        """Check if the model has a specific capability."""
        return capability in self.capabilities

    @property
    def has_vision(self) -> bool:
        """Check if the model supports vision (image understanding)."""
        return ModelCapability.vision in self.capabilities

    @property
    def has_video_understanding(self) -> bool:
        """Check if the model supports video understanding."""
        return ModelCapability.video_understanding in self.capabilities

    @property
    def has_audio_understanding(self) -> bool:
        """Check if the model supports audio understanding."""
        return ModelCapability.audio_understanding in self.capabilities

    @property
    def has_document_understanding(self) -> bool:
        """Check if the model supports document understanding."""
        return ModelCapability.document_understanding in self.capabilities


